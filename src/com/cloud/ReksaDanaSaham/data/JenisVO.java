package com.cloud.ReksaDanaSaham.data;

public class JenisVO {
	private int id;
	private String reksaDana;
	private String saham;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getReksaDana() {
		return reksaDana;
	}
	public void setReksaDana(String reksaDana) {
		this.reksaDana = reksaDana;
	}
	public String getSaham() {
		return saham;
	}
	public void setSaham(String saham) {
		this.saham = saham;
	}
	
}
